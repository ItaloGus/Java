            PreparedStatement pst = conecta.conn.prepareStatement("insert into tbprofessor(nome_prof,data_de_nascimento_prof,cpf_prof,celular_prof,endereco_prof,numero_de_matricula_prof,escolaridade,salario,admissao,naturalidade,eixo_de_atuacao,sexo) values(?,?,?,?,?,?,?,?,?,?,?,?)");
