            pst.setString(2, jFormattedTextFieldNascimento_Professor.getText());
            pst.setString(3, jFormattedTextFieldCPF_Professor.getText());
            pst.setString(4, jFormattedTextFieldCelular_Professor.getText());
            pst.setString(5, jTextEndereco_Professor.getText());
            pst.setString(6, jTextMatricula_Professor.getText());
