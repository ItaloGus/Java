     public void consultarNomes(){
        conecta.executaSQL("select * from tbprofessor order by cod_prof");
        try {
            conecta.rs.first();
             do{
              jComboBoxAlterar.addItem("(" + conecta.rs.getInt("cod_prof")+ ")" + " - " + conecta.rs.getString("nome_prof"));
        }while(conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao procurar nomes já cadastrasdos!\nErro:" + ex);
        }
    }
    
    public void pegarDados(){
       alconecta.executaSQL("select * from tbprofessor order by cod_prof");
       
        try {
            alconecta.rs.first();
             do{
                 if(jTextNome_Professor.getText().equals("(" + ")" + " - " + (String) alconecta.rs.getString("nome_prof"))){
                     jTextNome_Professor.setText((String) alconecta.rs.getString("nome_prof"));
                     jTextEixo_de_Atuacao_Professor.setText(alconecta.rs.getString("eixo_de_atuacao"));
                     jTextCod_Professor.setText(alconecta.rs.getString("cod_prof"));
                     jTextEndereco_Professor.setText((String) alconecta.rs.getString("endereco_prof"));
                     jTextMatricula_Professor.setText((String) alconecta.rs.getString("numero_de_matricula_prof"));
                     jTextNaturalidade_Professor.setText(alconecta.rs.getString("naturalidade"));
                     jTextEscolaridade_Professor.setText(alconecta.rs.getString("escolaridade"));
                     jFormattedTextFieldAno_de_Admissao_Professor.setText(alconecta.rs.getString("admissao"));
                     jFormattedTextFieldCPF_Professor.setText((String) alconecta.rs.getString("cpf_prof"));
                     jFormattedTextFieldCelular_Professor.setText((String) alconecta.rs.getString("celular_prof"));
                     jFormattedTextFieldNascimento_Professor.setText((String) alconecta.rs.getString("data_de_nascimento_prof"));
                     jFormattedTextFieldSalario_Professor.setText(alconecta.rs.getString("salario"));
                     jComboBoxSexo_Professor.setSelectedItem((String) alconecta.rs.getString("sexo"));
                 }
            }while(alconecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar dados!\nErro:" + ex);
        }
    }
    
