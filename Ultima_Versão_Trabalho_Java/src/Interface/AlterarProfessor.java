            pst.setString(9, jFormattedTextFieldAno_de_Admissao_Professor.getText());
            pst.setString(10, jTextNaturalidade_Professor.getText());
            pst.setString(11, jTextEixo_de_Atuacao_Professor.getText());
            pst.setString(12, (String) jComboBoxSexo_Professor.getSelectedItem());
