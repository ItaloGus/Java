    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            PreparedStatement pst = con.conn.prepareStatement("insert into tbestoque(produto_estoque, quantidade_estoque, data_chegada_estoque, data_validade_estoque, responsavel_estoque) values (?,?,?,?,?)");
            pst.setString(1, (String) jTextFieldProdutoEstoque.getText());
            pst.setInt(2, Integer.parseInt(((jFormattedTextFieldQuantidadeEstoque.getText()))));
            pst.setString(3, (String) jTextFieldDataChegadaEstoque.getText());
            pst.setString(4, (String) jTextFieldValidadeEstoque.getText());
            pst.setString(5, (String) jFormattedTextFieldResponsavelEstoque.getText());      
            pst.execute();
            
            JOptionPane.showMessageDialog(this, "Estoque Registrado com sucesso!", "Atualizando Tabela", INFORMATION_MESSAGE);
            // TODO add your handling code here:
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "N√£o foi possivel cadastrar o produto!\n    Erro:" + ex.getMessage(), "Error", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

