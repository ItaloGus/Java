    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            PreparedStatement pst = con.conn.prepareStatement("insert into tbfuncionario(nome_funcionario, idade_funcionario, cpf_funcionario, endereco_funcionario, telefone_funcionario) values (?,?,?,?,?)");
            pst.setString(1, (String) jTextFieldNomeFuncionario.getText());
            pst.setInt(2, Integer.parseInt(((jFormattedTextFieldIdadeFuncionario.getText()))));
            pst.setString(3, (String) jTextFieldCPFFuncionario.getText());
            pst.setString(4, (String) jFormattedTextFieldEnderecoFuncionario.getText());
            pst.setString(5, (String) jTextFieldTelefoneFuncionario.getText());      
            pst.execute();
            JOptionPane.showMessageDialog(this, "Funcionário cadastrado com sucesso!", "Atualizando Tabela", INFORMATION_MESSAGE);
            // TODO add your handling code here:
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Não foi possivel cadastrar o produto!\n    Erro:" + ex.getMessage(), "Error", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

